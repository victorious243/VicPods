function translateUserMessage(message, language) {
  const normalized = normalizeLanguage(language);
  const source = String(message || '').trim();
  const staticEntry = MESSAGE_TRANSLATIONS[source];

  if (staticEntry) {
    return staticEntry[normalized] || source;
  }

  return translateDynamicMessage(source, normalized);
}


function normalizeLanguage(value) {
  const normalized = String(value || '').trim().toLowerCase();
  return LANGUAGE_META[normalized] ? normalized : 'en';
}

function resolveLanguagePreference(user) {
  return normalizeLanguage(user?.languagePreference);
}

function getLanguageOptions() {
  return Object.values(LANGUAGE_META).map((item) => ({
    value: item.code,
    label: item.label,
  }));
}

function resolveAiLanguageName(language) {
  return LANGUAGE_META[normalizeLanguage(language)].aiLabel;
}

function interpolate(template, values) {
  return Object.keys(values || {}).reduce((acc, key) => (
    acc.replace(new RegExp(`\\{${key}\\}`, 'g'), String(values[key]))
  ), template);
}

function translate(key, language, fallback = '') {
  const table = TRANSLATIONS[key];
  if (!table) {
    return fallback || key;
  }

  const normalized = normalizeLanguage(language);
  return table[normalized] || table.en || fallback || key;
}

